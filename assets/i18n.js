  // ─── Engine ───────────────────────────────────────────────────────────────
  var LANG_KEY = 'lang';
  var currentLang = localStorage.getItem(LANG_KEY) || 'en';

  function applyLang(lang) {
    currentLang = lang;
    localStorage.setItem(LANG_KEY, lang);
    document.documentElement.setAttribute('lang', lang === 'zh' ? 'zh-Hans' : 'en');

    document.querySelectorAll('[data-i18n]').forEach(function (el) {
      var key = el.getAttribute('data-i18n');
      var entry = T[key];
      var isHtml = el.getAttribute('data-i18n-mode') === 'html';

      if (lang === 'zh') {
        if (!entry) return;
        if (isHtml) { el.innerHTML = entry.zh; }
        else { el.textContent = entry.zh; }
      } else {
        var cached = el.getAttribute('data-i18n-en');
        if (cached !== null) {
          if (isHtml) { el.innerHTML = cached; }
          else { el.textContent = cached; }
        }
      }
    });

    var btn = document.getElementById('lang-toggle');
    if (btn) {
      var enSpan = btn.querySelector('[data-lang-en]');
      var zhSpan = btn.querySelector('[data-lang-zh]');
      if (enSpan) enSpan.style.fontWeight = lang === 'en' ? '800' : '400';
      if (zhSpan) zhSpan.style.fontWeight = lang === 'zh' ? '800' : '400';
    }
  }

  function cacheEnglish() {
    document.querySelectorAll('[data-i18n]').forEach(function (el) {
      if (!el.hasAttribute('data-i18n-en')) {
        var isHtml = el.getAttribute('data-i18n-mode') === 'html';
        el.setAttribute('data-i18n-en', isHtml ? el.innerHTML : el.textContent);
      }
    });
  }

  function injectToggle() {
    var navlinks = document.querySelector('.navlinks');
    if (!navlinks) return;
    var btn = document.createElement('button');
    btn.id = 'lang-toggle';
    btn.className = 'btn pill lang-toggle';
    btn.setAttribute('aria-label', 'Switch language / 切换语言');
    btn.innerHTML =
      '<span data-lang-en>EN</span>' +
      '<span style="opacity:0.35;margin:0 2px;">|</span>' +
      '<span data-lang-zh>中</span>';
    btn.addEventListener('click', function () {
      applyLang(currentLang === 'en' ? 'zh' : 'en');
    });
    var themeBtn = navlinks.querySelector('.theme-toggle');
    if (themeBtn) { navlinks.insertBefore(btn, themeBtn); }
    else { navlinks.appendChild(btn); }
  }

  cacheEnglish();
  injectToggle();
  applyLang(currentLang);

